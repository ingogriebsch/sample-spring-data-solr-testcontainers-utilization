package com.github.ingogriebsch.sample.spring.data.solr.testcontainers.utilization.solr;

import org.apache.solr.client.solrj.SolrClient;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.solr.core.SolrTemplate;
import org.springframework.data.solr.repository.config.EnableSolrRepositories;

@Configuration
@EnableSolrRepositories(basePackages = "com.github.ingogriebsch.sample.spring.data.solr.testcontainers.utilization.repository",
    multicoreSupport = true)
public class SolrConfiguration {

    @Bean
    public SolrTemplate solrTemplate(SolrClient solrClient) {
        return new SolrTemplate(solrClient);
    }

}
